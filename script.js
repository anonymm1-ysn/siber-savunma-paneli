function aiBeyni(mesaj) {
    let temiz = mesaj.toLowerCase()
        .replace(/[?.!]/g, "")
        .replace(/ğ/g, "g").replace(/ü/g, "u").replace(/ş/g, "s")
        .replace(/ı/g, "i").replace(/ö/g, "o").replace(/ç/g, "c").trim();

    const anahtarlar = Object.keys(ARCHI_CORE);

    // 1. CÜMLE İÇİNDE KELİME YAKALAMA
    for (let anahtar of anahtarlar) {
        if (temiz.includes(anahtar)) return ARCHI_CORE[anahtar];
    }

    // 2. RANDOM ANALİZİ
    if (temiz.length > 5 && !/[aeiou]/.test(temiz)) {
        return "Kanka klavyede resmen sanatsal bir çalışma yapmışsın! 'eifhawle' falan derken zekanın sınırlarını zorluyorsun, bayıldım bu tarza!";
    }

    return null;
}

